<script lang="ts">
import * as Vue from 'vue';

import { getContrastedColor } from '@/lib/colors';
import sanitizeMessage from '@/lib/sanitizeMessage';
import Message from '@/interfaces/Message';
import SkAvatar from '@/components/SkAvatar.vue';
import SkUserInfo from '@/components/SkUserInfo.vue';

interface ChatCardData {
  sanitizedMessage: string;
}

export default Vue.defineComponent({
  components: {
    SkAvatar,
    SkUserInfo,
  },
  props: {
    tags: {
      type: Object as Vue.PropType<Message>,
    },
    isHypeChat: {
      type: Boolean,
      default: false,
    },
    /**
     * The color of message text.
     */
    color: {
      type: String,
      default: '#FFFFFF',
    },
    /**
     * The color of team.
     */
    teamColor: String,
    /**
     * The background color of the card.
     */
    backgroundColor: {
      type: String,
      default: '#18181B',
    },
    /**
     * The color of the username / team badge.
     */
    userColor: {
      type: String,
      default: '#FFFFFF',
    },
    /**
     * The channel id the message was sent in. Ssed to retrieve channel specific badges.
     */
    channelId: String,
    /**
     * The username to display.
     */
    name: String,
    /**
     * The alt-name to display.
     */
    altName: String,
    /**
     * The avatar image to display.
     */
    image: String,
    /**
     * The raw badge string from twitch irc.
     */
    badges: String,
    /**
     * 2 Character Country code. Used to display a flag next to the users name.
     */
    countryCode: String,
    /**
     * The name of the team badge. Can be any valid font awesome brand name.
     */
    team: String,
    /**
     * The pronoun to display
     */
    pronoun: String,
    /**
     * The message text to display.
     */
    message: String,
    /**
     * The time the message was sent.
     */
    createdAt: String,
    /**
     * Set to true to sanitize the message with DOMPurify
     */
    trustMessage: {
      type: Boolean,
      default: false,
    },
    /**
     * Set to true if the message text color should be the same as the user color
     */
    me: Boolean,
    announcement: {
      type: Boolean,
      required: false,
    },
    announcementColor: {
      type: String,
      required: false,
    },
    channelPrimaryColor: {
      type: String,
      required: false,
    },
  },
  computed: {
    formattedHypeChatAmount() {
      if (!this.$props.tags?.pinned_chat_paid_level) return '';
      return (Number(this.$props.tags.pinned_chat_paid_canonical_amount) * parseFloat(`1e-${this.$props.tags.pinned_chat_paid_exponent}`)).toFixed(2);
    },
    sanitizedMessage(): string {
      return sanitizeMessage(this.$props.message || '', this.$props.trustMessage);
    },
    contrastedColor(): string {
      return getContrastedColor(this.$props.userColor, this.$props.backgroundColor);
    },
  },
});
</script>

